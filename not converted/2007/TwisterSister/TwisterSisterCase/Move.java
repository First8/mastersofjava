/**
 * Holds the different moves from the dance routine. 
 */
public enum Move {
    
	/** steps become 200% bigger. */ 
	BIGGER,
	/** steps become 50% smaller. */
	SMALLER,
	/** turn counter clockwise. */
	TURN_LEFT,
	/** turn clockwise. */
	TURN_RIGHT,
	/** move one step to the left. */
	STEP_LEFT,
	/** move one step to the right. */
	STEP_RIGHT;
	
}
