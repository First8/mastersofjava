
public class Clue {
	
	public String clue;
	public String answer;
	public boolean horizontal;
	public int x;
	public int y;
	
	public Clue(String clue, String answer, boolean horizontal, int x, int y) {
		super();
		this.clue = clue;
		this.answer = answer;
		this.horizontal = horizontal;
		this.x = x;
		this.y = y;
	}
	
	
}
